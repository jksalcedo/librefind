@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun IgnoredAppsScreen(
    onBackClick: () -> Unit,
    viewModel: IgnoredAppsViewModel = koinViewModel()
) {
    val ignoredApps by viewModel.ignoredApps.collectAsState()

    Scaffold(
        topBar = {
            TopAppBar(
                title = { Text("Ignored Apps") },
                navigationIcon = {
                    IconButton(onClick = onBackClick) {
                        Icon(Icons.AutoMirrored.Filled.ArrowBack, contentDescription = "Back")
                    }
                }
            )
        }
    ) { padding ->
        if (ignoredApps.isEmpty()) {
            Box(
                modifier = Modifier
                    .fillMaxSize()
                    .padding(padding),
                contentAlignment = Alignment.Center
            ) {
                Text(
                    text = "No ignored apps. \n" +
                            "Long-press any app in the list â†’ tap Ignore.",
                    style = MaterialTheme.typography.bodyLarge,
                    color = MaterialTheme.colorScheme.onSurfaceVariant,
                    textAlign = TextAlign.Center
                )
            }
        } else {
            LazyColumn(
                modifier = Modifier
                    .fillMaxSize()
                    .padding(padding),
                contentPadding = PaddingValues(16.dp),
                verticalArrangement = Arrangement.spacedBy(8.dp)
            ) {
                items(ignoredApps, key = { it.packageName }) { app ->
                    IgnoredAppRow(
                        app = app,
                        onRestoreClick = { viewModel.restoreApp(app.packageName) }
                    )
                }
            }
        }
    }
}

@Composable
private fun IgnoredAppRow(
    app: IgnoredAppEntity,
    onRestoreClick: () -> Unit
) {
    val context = LocalContext.current
    val appLabel = try {
        context.packageManager.getApplicationLabel(
            context.packageManager.getApplicationInfo(app.packageName, 0)
        ).toString()
    } catch (_: Exception) {
        app.packageName
    }

    Card(
        modifier = Modifier.fillMaxWidth(),
        elevation = CardDefaults.cardElevation(defaultElevation = 2.dp)
    ) {
        Row(
            modifier = Modifier
                .fillMaxWidth()
                .padding(16.dp),
            verticalAlignment = Alignment.CenterVertically
        ) {

            AsyncImage(
                model = context.packageManager.getApplicationIcon(app.packageName),
                contentDescription = "App Icon",
                modifier = Modifier.size(40.dp)
            )

            Spacer(modifier = Modifier.width(12.dp))

            Column(modifier = Modifier.weight(1f)) {
                Text(
                    text = appLabel,
                    style = MaterialTheme.typography.titleMedium,
                    fontWeight = FontWeight.SemiBold,
                    maxLines = 1,
                    overflow = TextOverflow.Ellipsis
                )
                Text(
                    text = app.packageName,
                    style = MaterialTheme.typography.bodySmall,
                    color = MaterialTheme.colorScheme.onSurfaceVariant,
                    maxLines = 1,
                    overflow = TextOverflow.Ellipsis
                )
            }

            TextButton(onClick = onRestoreClick) {
                Icon(
                    Icons.Default.Refresh,
                    contentDescription = null,
                    modifier = Modifier.size(18.dp)
                )
                Spacer(modifier = Modifier.width(4.dp))
                Text("Restore")
            }
        }
    }
}
